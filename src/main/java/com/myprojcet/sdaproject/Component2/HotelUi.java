     public void hotelClassDiagBuild() throws IOException{
        
        DisplayImage display = new DisplayImage();
        
        if(scaleHotel.equalsIgnoreCase("small")){
        
            
           if(restaurantsHotel.equalsIgnoreCase("Available")){
               
                display.displayImg("C:/UML/hotel/Class/2.jpg");
           }
           else if(restaurantsHotel.equalsIgnoreCase("Not Available")){

                display.displayImg("C:/UML/hotel/Class/4.png");
           }
           else{
               JOptionPane.showMessageDialog(null, "Requirements insufficient for diagram generation",
                        "Diagram not found", JOptionPane.PLAIN_MESSAGE);
           }
               
        }else if(scaleHotel.equalsIgnoreCase("medium")){
            
            if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Available")){
                 
                display.displayImg("C:/UML/hotel/Class/5.jpg");
            }
            else if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Not Available")){
            
                display.displayImg("C:/UML/hotel/Class/6.jpg");
            }
            else{
                 display.displayImg("C:/UML/hotel/Class/4.png");
            }
        }else{
            
            if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Available") && flightHotel.equalsIgnoreCase("Available")){
                 
                display.displayImg("C:/UML/hotel/Class/7.jpg");
            }
            else if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Available") && flightHotel.equalsIgnoreCase("Not Available")){
            
                 display.displayImg("C:/UML/hotel/Class/8.jpg");
            }
            else if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Not Available") && flightHotel.equalsIgnoreCase("Available")){
            
                display.displayImg("C:/UML/hotel/Class/9.jpg");
            }
            else if(restaurantsHotel.equalsIgnoreCase("Available") && taxiHotel.equalsIgnoreCase("Not Available") && flightHotel.equalsIgnoreCase("Not Available")){
            
                 display.displayImg("C:/UML/hotel/Class/10.jpg");
            }
            else{
                
                 display.displayImg("C:/UML/hotel/Class/11.jpg");
            }
            
        }
    }

