    private void scaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scaleActionPerformed
        // TODO add your handling code here:
        scaleHotel = scale.getItemAt(scale.getSelectedIndex());
        if(scaleHotel.equalsIgnoreCase("small")){
            
            flight.setVisible(false);
            taxi.setVisible(false);  
            jLabel5.setVisible(false);
            jLabel7.setVisible(false);
            
        }else if(scaleHotel.equalsIgnoreCase("medium")){
            
            flight.setVisible(false);
            jLabel5.setVisible(false);
            taxi.setVisible(true);
            jLabel7.setVisible(true);
        }
        else{
            
            flight.setVisible(true);
            taxi.setVisible(true);  
            jLabel5.setVisible(true);
            jLabel7.setVisible(true);
        }
    }//GEN-LAST:event_scaleActionPerformed

